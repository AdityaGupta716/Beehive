import { useState } from "react";
import { useNavigate } from "react-router-dom";
import { saveToken } from "../../utils/auth";
import { apiFetch } from "../../utils/apiFetch";

const SignUpPage = () => {
  const navigate = useNavigate();

  const [email, setEmail] = useState("");
  const [otp, setOtp] = useState("");
  const [username, setUsername] = useState("");
  const [password, setPassword] = useState("");
  const [step, setStep] = useState<"email" | "otp" | "password" | "complete">(
    "email",
  );

  const [loading, setLoading] = useState(false);
  const [error, setError] = useState("");

  const requestOtp = async () => {
    setLoading(true);
    setError("");

    try {
      await apiFetch("/api/auth/request-otp", {
        method: "POST",
        body: JSON.stringify({ email }),
      });

      // if apiFetch didn't throw → success
      setStep("otp");
    } catch (err: any) {
      setError(err.message || "Failed to send OTP");
    } finally {
      setLoading(false);
    }
  };

  const verifyOtp = async () => {
    setLoading(true);
    setError("");

    try {
      await apiFetch("/api/auth/verify-otp", {
        method: "POST",
        body: JSON.stringify({ email, otp }),
      });

      // success → move ahead
      setStep("complete");
    } catch (err: any) {
      console.error("VERIFY OTP ERROR:", err);
      setError(err.message || "Invalid or expired OTP");
    } finally {
      setLoading(false);
    }
  };

  const completeSignup = async () => {
  setLoading(true);
  setError("");

  try {
    const data = await apiFetch("/api/auth/complete-signup", {
      method: "POST",
      body: JSON.stringify({
        email,
        username,
        password,
      }),
    });

    saveToken(data.access_token);

    // auto redirect
    data.role === "admin"
      ? navigate("/admin")
      : navigate("/dashboard");

  } catch (err: any) {
    setError(err.message || "Failed to complete signup");
  } finally {
    setLoading(false);
  }
};

  const setPasswordAndSignup = async () => {
  setLoading(true);
  setError("");

  try {
    const data = await apiFetch("/api/auth/set-password", {
      method: "POST",
      body: JSON.stringify({ email, password }),
    });

    saveToken(data.access_token);

    data.role === "admin"
      ? navigate("/admin")
      : navigate("/dashboard");

  } catch (err: any) {
    setError(err.message || "Failed to create account");
  } finally {
    setLoading(false);
  }
};

  const googleSignIn = async () => {
  setError("");

  try {
    const data = await apiFetch("/api/auth/google", {
      method: "POST",
      body: JSON.stringify({ email: "test@gmail.com", name: "Google User" }),
    });

    saveToken(data.access_token);

    data.role === "admin"
      ? navigate("/admin")
      : navigate("/dashboard");

  } catch (err: any) {
    setError(err.message || "Google sign-in failed");
  }
};

  return (
    // Added text-gray-900 to ensure text visibility across all systems
    <div className="min-h-screen flex items-center justify-center bg-gray-100 px-4 text-gray-900">
      <div className="bg-white p-8 rounded-xl shadow-xl w-full max-w-md border border-gray-200">
        <h2 className="text-2xl font-bold mb-6 text-center text-gray-800">
          Sign Up
        </h2>

        {error && (
          <p className="text-red-600 text-sm mb-4 text-center font-medium">
            {error}
          </p>
        )}

        <div className="space-y-4">
          {/* EMAIL STEP */}
          {step === "email" && (
            <>
              <input
                type="email"
                placeholder="Email"
                value={email}
                onChange={(e) => setEmail(e.target.value)}
                className="input"
              />
              <button onClick={requestOtp}>Send OTP</button>
            </>
          )}

          {step === "otp" && (
            <>
              <input
                type="text"
                placeholder="Enter OTP"
                value={otp}
                onChange={(e) => setOtp(e.target.value)}
                className="input"
              />
              <button onClick={verifyOtp}>Verify OTP</button>
            </>
          )}

          {step === "complete" && (
            <>
              <input
                type="text"
                placeholder="Username"
                value={username}
                onChange={(e) => setUsername(e.target.value)}
                className="input"
              />

              <input
                type="password"
                placeholder="Password"
                value={password}
                onChange={(e) => setPassword(e.target.value)}
                className="input"
              />

              <button onClick={completeSignup}>Create Account</button>
            </>
          )}

          {/* PASSWORD STEP */}
          {step === "password" && (
            <>
              <input
                type="password"
                placeholder="Create password"
                className="w-full border border-gray-300 px-3 py-2 rounded-lg text-gray-900 bg-white focus:ring-2 focus:ring-amber-500 outline-none"
                value={password}
                onChange={(e) => setPassword(e.target.value)}
              />
              <button
                onClick={setPasswordAndSignup}
                disabled={loading}
                className="w-full bg-amber-500 hover:bg-amber-600 text-white font-semibold py-2 rounded-lg transition-colors disabled:bg-amber-300"
              >
                {loading ? "Creating..." : "Create Account"}
              </button>
            </>
          )}

          <div className="relative my-6">
            <div className="absolute inset-0 flex items-center">
              <div className="w-full border-t border-gray-300"></div>
            </div>
            <div className="relative flex justify-center text-sm">
              <span className="bg-white px-2 text-gray-500">or</span>
            </div>
          </div>

          <button
            onClick={googleSignIn}
            className="w-full border border-gray-300 py-2 rounded-lg flex items-center justify-center gap-2 hover:bg-gray-50 transition-colors text-gray-700 font-medium"
          >
            Continue with Google
          </button>
        </div>
      </div>
    </div>
  );
};

export default SignUpPage;
